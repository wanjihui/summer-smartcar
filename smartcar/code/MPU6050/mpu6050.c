/*********************************************************************************************************************
* 文件名称          mpu6050
* 适用平台          MM32F327X_G8P
* 描述              MPU6050 六轴陀螺仪/加速度计模块实现/目前为读取原始值*********************************************************************************************************************/

#include "mpu6050.h"

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     MPU6050 模块初始化
// 功能描述     初始化 MPU6050（带重试，最多5次）
//-------------------------------------------------------------------------------------------------------------------
void mpu6050_module_init(void)
{
    uint8_t retry = 0;
    while(mpu6050_init() && retry < 5)
    {
        retry++;
        system_delay_ms(500);
    }
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     PIT 中断回调
// 功能描述     在 TIM6 中断服务函数中调用，采集 MPU6050 加速度和角速度数据
//-------------------------------------------------------------------------------------------------------------------
void mpu6050_pit_callback(void)
{
    mpu6050_get_acc();      // 获取 MPU6050 加速度数据
    mpu6050_get_gyro();     // 获取 MPU6050 角速度数据
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取三轴加速度
// 返回参数     原始 ADC 值（可选调用 mpu6050_acc_transition() 转为物理单位 g）
//-------------------------------------------------------------------------------------------------------------------
int16 mpu6050_get_acc_x(void)  { return mpu6050_acc_x; }
int16 mpu6050_get_acc_y(void)  { return mpu6050_acc_y; }
int16 mpu6050_get_acc_z(void)  { return mpu6050_acc_z; }

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取三轴角速度
// 返回参数     原始 ADC 值（可选调用 mpu6050_gyro_transition() 转为物理单位 °/s）
//-------------------------------------------------------------------------------------------------------------------
int16 mpu6050_get_gyro_x(void) { return mpu6050_gyro_x; }
int16 mpu6050_get_gyro_y(void) { return mpu6050_gyro_y; }
int16 mpu6050_get_gyro_z(void) { return mpu6050_gyro_z; }
