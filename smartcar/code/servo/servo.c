/*********************************************************************************************************************
* 文件名称          servo
* 适用平台          MM32F327X_G8P
* 描述              舵机控制模块实现
*                   使用 TIM2_CH1 (PA15) 输出 50Hz PWM 控制舵机角度
*********************************************************************************************************************/

#include "servo.h"

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     舵机初始化
// 功能描述     配置 TIM2_CH1 (PA15) 为 PWM 输出，频率 50Hz，初始占空比 0
//              PWM 频率计算公式：120MHz / (PSC+1) / (ARR+1) = 50Hz
//              其中 PSC=73, ARR=32432（由 pwm_init 内部自动计算）
//-------------------------------------------------------------------------------------------------------------------
void servo_init(void)
{
    pwm_init(SERVO_PWM, SERVO_FREQ, 0);
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     设置舵机角度
// 参数说明     angle   目标角度（0.0° ~ 180.0°），自动限幅到 [SERVO_ANGLE_MIN, SERVO_ANGLE_MAX]
// 功能描述     将角度值转换为对应的 PWM 占空比并输出
//              0°   → 0.5ms 脉冲（占空比 250/10000）
//              90°  → 1.5ms 脉冲（占空比 750/10000）
//              180° → 2.5ms 脉冲（占空比 1250/10000）
//-------------------------------------------------------------------------------------------------------------------
void servo_set_angle(float angle)
{
    // 限幅保护
    if(angle < SERVO_ANGLE_MIN)
    {
        angle = SERVO_ANGLE_MIN;
    }
    if(angle > SERVO_ANGLE_MAX)
    {
        angle = SERVO_ANGLE_MAX;
    }

    // 角度 → 占空比 → 输出 PWM
    pwm_set_duty(SERVO_PWM, (uint32)SERVO_DUTY(angle));
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     舵机回中
// 功能描述     将舵机设置为 90° 中位
//-------------------------------------------------------------------------------------------------------------------
void servo_set_center(void)
{
    servo_set_angle(SERVO_ANGLE_CENTER);
}
