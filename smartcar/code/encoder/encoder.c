/*********************************************************************************************************************
* 文件名称          encoder
* 适用平台          MM32F327X_G8P
* 描述              双路正交编码器模块实现
*********************************************************************************************************************/

#include "encoder.h"

int16 encoder_left_speed  = 0;
int16 encoder_right_speed = 0;

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     编码器初始化
// 功能描述     初始化双路正交解码编码器 + PIT 周期中断
//-------------------------------------------------------------------------------------------------------------------
void encoder_init(void)
{
    // 初始化左编码器 正交解码模式 TIM3 B4/B5
    encoder_quad_init(ENCODER_LEFT, ENCODER_LEFT_A, ENCODER_LEFT_B);

    // 初始化右编码器 正交解码模式 TIM4 B6/B7
    encoder_quad_init(ENCODER_RIGHT, ENCODER_RIGHT_A, ENCODER_RIGHT_B);

    // 初始化 PIT 周期中断 用于定时采集编码器数据
    pit_ms_init(ENCODER_PIT, ENCODER_PIT_PERIOD_MS);
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取左编码器速度
// 返回参数     脉冲数/采样周期（ENCODER_PIT_PERIOD_MS ms 内的累计脉冲）
//-------------------------------------------------------------------------------------------------------------------
int16 encoder_get_left_speed(void)
{
    return encoder_left_speed;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取右编码器速度
// 返回参数     脉冲数/采样周期（ENCODER_PIT_PERIOD_MS ms 内的累计脉冲）
//-------------------------------------------------------------------------------------------------------------------
int16 encoder_get_right_speed(void)
{
    return encoder_right_speed;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     PIT 中断回调
// 功能描述     在 TIM6 中断服务函数中调用，读取并清零编码器计数值
//              encoder_quad_init 使用正交解码时，TIM->CNT 由硬件自动增减
//              encoder_get_count 读取 CNT 并清零，供下次采集
//-------------------------------------------------------------------------------------------------------------------
void encoder_pit_callback(void)
{
    encoder_left_speed  = encoder_get_count(ENCODER_LEFT);
    encoder_clear_count(ENCODER_LEFT);

    encoder_right_speed = encoder_get_count(ENCODER_RIGHT);
    encoder_clear_count(ENCODER_RIGHT);
}
